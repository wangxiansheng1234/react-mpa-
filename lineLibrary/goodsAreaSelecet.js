    //地区id
let firstAreaId, secondAreaId, thirdAreaId = -1;
let firstAreaName, secondAreaName, thirdAreaName = '';
let hasReset = false;

function initAreaId() {
    firstAreaId = 0;
    secondAreaId = 0;
    thirdAreaId = 0;
}

function initAreaName() {
    provinceList.map(function (item1) {
        if(item1.id == firstAreaId && firstAreaId > 0){
            firstAreaName = item1.regionName;
            if(secondAreaId > 0){
                item1.region.map(function (item2) {
                    if(item2.id == secondAreaId){
                        secondAreaName = item2.regionName;
                        if(thirdAreaId > 0){
                            item2.region.map(function (item3) {
                                if(item3.id == thirdAreaId){
                                    thirdAreaName = item3.regionName;
                                }
                            })
                        }
                    }
                })
            }
        }
    })
}

initAreaId();
initAreaName();

//点击地址栏，显示和隐藏下拉选框。
$('.select-area').click(function (e) {
    if($(this).hasClass('on')){
        $('.select-wrap').slideUp(100);
        $(this).removeClass('on');
    }else{
        $(this).addClass('on');
        $('.select-wrap').slideDown(100);
        if(!hasReset){
            initAreaWrap(1);
            initAreaWrap(2);
            initAreaWrap(3);
            initAreaTabs();
            $('.first-area').show();
            hasReset = true;
        }
    }
    e.stopPropagation();
});

$('.select-wrap').click(function (e) {
    e.stopPropagation();
});
//点击其他区域，地址选框消失
$('body').click(function () {
    if($('.select-area').hasClass('on')){
        $('.select-wrap').slideUp(100);
        $('.select-area').removeClass('on');
    }
});

//初始化tab
function initAreaTabs() {
    $('.select-tabs li').each(function (index) {
        if(index === 0 && firstAreaName){
            $(this).find('p').text(firstAreaName);
        }else if(index === 1 && secondAreaName){
            $(this).find('p').text(secondAreaName);
        }else if(index === 2 && thirdAreaName){
            $(this).find('p').text(thirdAreaName);
        }
    })
}

//初始化地址栏
function initAreaWrap(type) {
    let html = '';
    let selected = '';
    if(type === 1){
        $('.first-area li').empty();
        provinceList.map(function (item) {
            selected = item.id == firstAreaId ? 'selected' : '';
            html = '<span><a href="###" class="'+ selected +'" data-value="'+ item.id +'">' + item.regionName + '</a></span>';
            $('.first-area li').append(html);
        });
    }else if(type === 2){
        provinceList.map(function (item1) {
            if(item1.id == firstAreaId && item1.region &&  item1.region.length > 0){
                $('.second-area li').empty();
                item1.region.map(function (item) {
                    selected = item.id == secondAreaId ? 'selected' : '';
                    html = '<span><a href="###" class="'+ selected +'" data-value="'+ item.id +'">' + item.regionName + '</a></span>';
                    $('.second-area li').append(html);
                });
            }
        })
    }else if(type === 3){
        provinceList.map(function (item1) {
            if(item1.id == firstAreaId && item1.region && item1.region.length > 0){
                item1.region.map(function (item2) {
                    if(item2.id == secondAreaId && item2.region && item2.region.length > 0){
                        $('.third-area li').empty();
                        item2.region.map(function (item) {
                            selected = item.id == thirdAreaId ? 'selected' : '';
                            html = '<span><a href="###" class="'+ selected +'" data-value="'+ item.id +'">' + item.regionName + '</a></span>';
                            $('.third-area li').append(html);
                        })
                    }
                })
            }
        })

    }
}

$('.first-area li').delegate('a','click', function () {
    $('.first-area li a').removeClass('selected')
    $(this).addClass('selected');
    firstAreaId = $(this).attr('data-value');
    firstAreaName = $(this).text();
    secondAreaId = -1;
    thirdAreaId = -1;
    hasReset = true;
    //初始化第二页
    initAreaWrap(2);

    //重置tab文字
    $('.select-tabs li').eq(0).find('p').text(firstAreaName);
    $('.select-tabs li').eq(1).find('p').text('请选择');
    $('.select-tabs li').eq(2).find('p').text('请选择');

    //重置tab选中状态
    $('.select-tabs li').removeClass('current');
    $('.select-tabs li').eq(1).addClass('current');

    $('.first-area').hide();
    $('.second-area').show();
});

$('.second-area li').delegate('a','click', function () {
    $('.second-area li a').removeClass('selected')
    $(this).addClass('selected');
    secondAreaId = $(this).attr('data-value');
    secondAreaName = $(this).text();
    thirdAreaId = -1;
    hasReset = true;

    initAreaWrap(3);

    $('.select-tabs li').eq(1).find('p').text(secondAreaName);
    $('.select-tabs li').eq(2).find('p').text('请选择');
    $('.select-tabs li').removeClass('current');
    $('.select-tabs li').eq(2).addClass('current');

    $('.second-area').hide();
    $('.third-area').show();
});

$('.third-area li').delegate('a','click', function () {
    $('.third-area li a').removeClass('selected')
    $(this).addClass('selected');
    thirdAreaId = $(this).attr('data-value');
    thirdAreaName = $(this).text();
    hasReset = true;

    $('.select-tabs li').eq(2).find('p').text(thirdAreaName)

    $('.select-wrap').slideUp(100);
    $('.select-area').removeClass('on');
    $('.select-area').find('del').text(firstAreaName + ' ' + secondAreaName + ' ' + thirdAreaName);
    console.log("first:" + firstAreaId + "  second:" + secondAreaId + "   third:" + thirdAreaId)
});

