$('.select-tabs li').click(function () {
    if($(this).find('p').text() === '请选择'){
        return;
    }
    if($(this).index() === 0){
        $('.first-area').show();
        $('.second-area').hide();
        $('.third-area').hide();
    }else if($(this).index() === 1){
        $('.first-area').hide();
        $('.second-area').show();
        $('.third-area').hide();
    }else{
        $('.first-area').hide();
        $('.second-area').hide();
        $('.third-area').show();
    }
    $('.select-tabs li').removeClass('current');
    $(this).addClass('current');
});

